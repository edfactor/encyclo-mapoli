namespace Demoulas.ProfitSharing.Common.Enums;

/// <summary>
/// Defines comparison operators for filtering numeric values.
/// </summary>
public enum ComparisonOperator
{
    /// <summary>
    /// Equality comparison (==)
    /// </summary>
    Equals = 0,

    /// <summary>
    /// Less than comparison (&lt;)
    /// </summary>
    LessThan = 1,

    /// <summary>
    /// Less than or equal comparison (&lt;=)
    /// </summary>
    LessThanOrEqual = 2,

    /// <summary>
    /// Greater than comparison (&gt;)
    /// </summary>
    GreaterThan = 3,

    /// <summary>
    /// Greater than or equal comparison (&gt;=)
    /// </summary>
    GreaterThanOrEqual = 4
}
